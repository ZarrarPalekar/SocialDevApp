// @route  GET api/profile/me
// @desc   Get current user's profile
// @access Private

// async here because we are using mongoose which returns a promise
router.get("/me", auth, async (req, res) => {
  try {
    // user pertains to Profile model which is referring to User model ObjectId
    // req.user.id is from auth token
    const profile = await (
      await Profile.findOne({ user: req.user.id })
    ).populate("user", ["name", "avatar"]); // .populate populates from user model

    if (!profile) {
      return res.status(400).json({ msg: "There is no profile for this user" });
    }

    res.json(profile);
  } catch (err) {
    console.error(err.message);
    res.status(500).send("Server Error");
  }
});

